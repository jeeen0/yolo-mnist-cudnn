"""
Auto-label the handwritten photos and build a YOLO detection dataset.

The photos are one digit on white paper, so the digit bounding box can be
found automatically (same digit_mask / digit_bbox used by preprocess.py).
No manual labeling needed: ~139 images become a labeled dataset in seconds.

Design choice: a SINGLE class "digit". YOLO's job here is detection (where is
the digit), not classification (which digit) -- mnistCUDNN does the latter.
A single-class detector is faster and generalizes better across 1/3/5/6/8.

Output layout (Ultralytics format):
  data/yolo/images/{train,val}/*.jpg
  data/yolo/labels/{train,val}/*.txt     # one line: 0 cx cy w h  (normalized)
  data/yolo/data.yaml

Run from repo root:  python scripts/01_make_dataset.py
"""

import os
import sys
import glob
import shutil
import random

import cv2

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from preprocess import digit_mask, digit_bbox  # noqa: E402

RAW_DIRS = {"6": "data/raw/6", "8": "data/raw/8"}  # extend with 1/3/5 if added
OUT = "data/yolo"
VAL_FRAC = 0.15
SEED = 1234


def yolo_line(box, W, H):
    """Convert a pixel bbox to a normalized YOLO label line (class 0)."""
    x, y, w, h = box
    cx = (x + w / 2.0) / W
    cy = (y + h / 2.0) / H
    return f"0 {cx:.6f} {cy:.6f} {w / W:.6f} {h / H:.6f}\n"


def main():
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    os.chdir(root)

    for split in ("train", "val"):
        os.makedirs(f"{OUT}/images/{split}", exist_ok=True)
        os.makedirs(f"{OUT}/labels/{split}", exist_ok=True)

    items = []
    for label, d in RAW_DIRS.items():
        files = []
        for ext in ("*.jpg", "*.jpeg", "*.png", "*.JPG", "*.JPEG", "*.PNG"):
            files += glob.glob(os.path.join(d, ext))
        for f in sorted(files):
            items.append((label, f))

    if not items:
        print("No raw images found under data/raw/{6,8}/. "
              "Put the photos there first.")
        sys.exit(1)

    random.Random(SEED).shuffle(items)
    n_val = int(len(items) * VAL_FRAC)
    val_set = set(range(n_val))

    kept, skipped = 0, 0
    for i, (label, path) in enumerate(items):
        img = cv2.imread(path, cv2.IMREAD_COLOR)
        if img is None:
            skipped += 1
            continue
        H, W = img.shape[:2]
        mask = digit_mask(cv2.cvtColor(img, cv2.COLOR_BGR2GRAY))
        box = digit_bbox(mask)
        if box is None:
            print("  no digit found, skipping:", path)
            skipped += 1
            continue

        split = "val" if i in val_set else "train"
        stem = f"{label}_{i:04d}"
        cv2.imwrite(f"{OUT}/images/{split}/{stem}.jpg", img)
        with open(f"{OUT}/labels/{split}/{stem}.txt", "w") as fh:
            fh.write(yolo_line(box, W, H))
        kept += 1

    with open(f"{OUT}/data.yaml", "w") as fh:
        fh.write(
            "# auto-generated by 01_make_dataset.py\n"
            f"path: {os.path.abspath(OUT)}\n"
            "train: images/train\n"
            "val: images/val\n"
            "nc: 1\n"
            "names: ['digit']\n"
        )

    print(f"dataset built: {kept} labeled, {skipped} skipped")
    print(f"  train + val under {OUT}/  ->  data.yaml ready")


if __name__ == "__main__":
    main()
